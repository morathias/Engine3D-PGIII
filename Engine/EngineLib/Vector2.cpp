//==================================================================================
Vector2::Vector2(float x, float y){
	_x = x;
	_y = y;
}
//==================================================================================
Vector2::~Vector2(void){
}
//==================================================================================
float Vector2::x(){
	return _x;
}
float Vector2::y(){
	return _y;
}
//==================================================================================
Vector2 Vector2::Normalize(){
	Vector2 normalized(1,1);
	return normalized;
}
//==================================================================================
float Vector2::getMagnitud(){
	return sqrt((_x * _x) + (_y * _y));
}
